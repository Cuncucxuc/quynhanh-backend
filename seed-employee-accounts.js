async function main() {
  const pool = process.env.MYSQL_URL
    ? mysql.createPool({
        uri: process.env.MYSQL_URL,
        waitForConnections: true,
        connectionLimit: 10,
        queueLimit: 0,
      })
    : mysql.createPool({
        host: process.env.DB_HOST || 'localhost',
        user: process.env.DB_USER || 'root',
        password: process.env.DB_PASSWORD || '123456',
        database: process.env.DB_NAME || 'quanlynhansu',
        waitForConnections: true,
        connectionLimit: 10,
        queueLimit: 0,
      });

  const conn = await pool.getConnection();
  try {
    await conn.beginTransaction();

    const [employeeCols] = await conn.query(`SHOW COLUMNS FROM employees`);
    const employeeColNames = employeeCols.map((c) => c.Field);
    if (!employeeColNames.includes('profileData')) {
      await conn.query(`ALTER TABLE employees ADD COLUMN profileData TEXT NULL`);
    }
    const [userCols] = await conn.query(`SHOW COLUMNS FROM users`);
    const userColNames = userCols.map((c) => c.Field);
    if (!userColNames.includes('password')) {
      throw new Error('Bảng users thiếu cột password');
    }

    for (const emp of employees) {
      await conn.query('INSERT IGNORE INTO departments (name) VALUES (?)', [emp.department]);

      const [existingUser] = await conn.query('SELECT email FROM users WHERE email = ?', [emp.email]);
      if (existingUser.length === 0) {
        await conn.query(
          'INSERT INTO users (email, fullName, password, role) VALUES (?, ?, ?, ?)',
          [emp.email.toLowerCase(), emp.fullName, emp.password, 'employee']
        );
      }

      const [existingEmp] = await conn.query('SELECT id FROM employees WHERE email = ?', [emp.email]);
      const schedule = JSON.stringify({
        'Thứ 2': 'Ca Sáng',
        'Thứ 3': 'Ca Sáng',
        'Thứ 4': 'Ca Sáng',
        'Thứ 5': 'Ca Chiều',
        'Thứ 6': 'Ca Sáng',
        'Thứ 7': 'Nghỉ',
        'Chủ Nhật': 'Nghỉ',
      });
      const profileData = JSON.stringify(emp.profileFields);

      if (existingEmp.length === 0) {
        const empId = `emp_${Date.now()}_${Math.floor(Math.random() * 1000)}`;
        const empCode = `NV_${Math.floor(1000 + Math.random() * 9000)}`;
        await conn.query(
          `INSERT INTO employees
           (id, employeeCode, fullName, gender, email, phone, address, department, position, joinDate, salary, bonus, penalty, notes, weeklySchedule, profileData)
           VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)` ,
          [
            empId,
            empCode,
            emp.fullName,
            emp.gender,
            emp.email.toLowerCase(),
            emp.phone,
            emp.address,
            emp.department,
            emp.position,
            new Date(),
            emp.salary,
            emp.bonus,
            emp.penalty,
            emp.notes,
            schedule,
            profileData,
          ]
        );
      } else {
        await conn.query(
          `UPDATE employees SET
            fullName = ?, gender = ?, phone = ?, address = ?, department = ?, position = ?,
            salary = ?, bonus = ?, penalty = ?, notes = ?, weeklySchedule = ?, profileData = ?
           WHERE email = ?`,
          [
            emp.fullName,
            emp.gender,
            emp.phone,
            emp.address,
            emp.department,
            emp.position,
            emp.salary,
            emp.bonus,
            emp.penalty,
            emp.notes,
            schedule,
            profileData,
            emp.email.toLowerCase(),
          ]
        );
      }
    }

    await conn.commit();
    console.log('Đã thêm/cập nhật 5 nhân viên đầy đủ thông tin thành công.');
  } catch (error) {
    await conn.rollback();
    console.error('Seed thất bại:', error);
    process.exitCode = 1;
  } finally {
    conn.release();
    await pool.end();
  }
}

main();